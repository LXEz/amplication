import * as React from "react";

import {
  Edit,
  SimpleForm,
  EditProps,
  TextInput,
  ReferenceArrayInput,
  SelectArrayInput,
} from "react-admin";

import { ToDoTaskTitle } from "../toDoTask/ToDoTaskTitle";

export const ToDoUserEdit = (props: EditProps): React.ReactElement => {
  return (
    <Edit {...props}>
      <SimpleForm>
        <TextInput label="Email" source="email" type="email" />
        <TextInput label="lookup" source="lookup" />
        <TextInput label="Name" source="name" />
        <ReferenceArrayInput source="toDoTasks" reference="ToDoTask">
          <SelectArrayInput
            optionText={ToDoTaskTitle}
            parse={(value: any) => value && value.map((v: any) => ({ id: v }))}
            format={(value: any) => value && value.map((v: any) => v.id)}
          />
        </ReferenceArrayInput>
      </SimpleForm>
    </Edit>
  );
};
